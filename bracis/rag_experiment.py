def compute_metrics(preds, refs):

    meteor = np.mean([
        meteor_score([r.split()], p.split())
        for p, r in zip(preds, refs)
    ])

    P, R, F1 = bertscore(preds, refs, lang="pt")
    bert_f1 = F1.mean().item()

    bleu = bleu_metric.compute(
        predictions=preds,
        references=[[r] for r in refs]
    )["score"]

    rouge = rouge_metric.compute(
        predictions=preds,
        references=refs
    )

    rouge_l = rouge["rougeL"]

    bleurt_scores = bleurt_scorer.score(references=refs, candidates=preds)
    bleurt_mean = float(np.mean(bleurt_scores))

    return {
        "METEOR": meteor,
        "BERTScore-F1": bert_f1,
        "BLEU": bleu,
        "ROUGE-L": rouge_l,
        "BLEURT": bleurt_mean
    }

