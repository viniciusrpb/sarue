def match_location(q_loc, doc_loc):
    return q_loc in doc_loc or doc_loc in q_loc


def geo_distance(lat1, lon1, lat2, lon2):
    return np.sqrt((lat1 - lat2)**2 + (lon1 - lon2)**2)

def geo_score(query_loc, doc):

    if not query_loc:
        return 0

    doc_locs = [normalize(l) for l in doc["metadata"].get("localidades", [])]

    for loc in doc_locs:
        if query_loc in loc or loc in query_loc:
            return 1.0

    return -0.5

def retrieve_vanilla(query, docs, index, k=5):

    q_emb = model.encode([query])
    faiss.normalize_L2(q_emb)

    D, I = index.search(q_emb, k)

    return [docs[i] for i in I[0]]


