    cache = {}

    for mode in ["vanilla", "hybrid", "graph","zero"]:  # adicione "vanilla", "hybrid" se quiser for mode in ["zero", "vanilla", "hybrid", "graph"]:

        print(f"\n========== MODE: {mode} ==========")

