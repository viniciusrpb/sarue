def normalize(s):
    s = re.sub(r"\s+", " ", s.strip())
    return ''.join(
        c for c in unicodedata.normalize('NFD', s.lower())
        if unicodedata.category(c) != 'Mn'
    )

def extract_entities(text, metadata=None):

    entities = []

    # =========================
    # 1. REGEX (alta precisão)
    # =========================
    patterns = {
        "DOC_ID": r"\b(Contrato|Conv[eê]nio|Aviso|Doação|Licitação|Credenciamento|Portaria)\s*(nº|no|n\.)?\s*\d+/\d{4}\b",
        "PROCESS_ID": r"\b\d{4,7}-\d{2}\.\d{4}\.\d\.\d{2}\.\d{4}\b",
    }

    for label, pattern in patterns.items():
        for m in re.finditer(pattern, text, re.IGNORECASE):
            entities.append((normalize(m.group()), label))

    # =========================
    # 2. METADATA (ouro)
    # =========================
    if metadata:

        for loc in metadata.get("localidades", []):
            entities.append((normalize(loc), "LOCATION"))

        for unit in metadata.get("tipo_unidade", []):
            entities.append((normalize(unit), "ORG"))

        for topic in metadata.get("topicos", []):
            entities.append((normalize(topic), "PROGRAM"))

    # =========================
    # 3. spaCy (NER geral)
    # =========================
    doc = nlp(text)

    for ent in doc.ents:

        label = ent.label_

        if label in ["LOC", "GPE"]:
            entities.append((normalize(ent.text), "LOCATION"))

        elif label == "ORG":
            entities.append((normalize(ent.text), "ORG"))

        # cuidado: spaCy em PT não tem label "FAC" muito confiável

    # =========================
    # 4. REGRAS DE DOMÍNIO (CRÍTICO)
    # =========================
    health_patterns = [
        r"\bUBS\s+[A-ZÁ-Úa-zá-ú\s]+",
        r"\bUPA\s+[A-ZÁ-Úa-zá-ú\s]+",
        r"\bHospital\s+[A-ZÁ-Úa-zá-ú\s]+",
        r"\bCl[ií]nica\s+[A-ZÁ-Úa-zá-ú\s]+",
        r"\bCentro de Sa[uú]de\s+[A-ZÁ-Úa-zá-ú\s]+",
    ]

    for pattern in health_patterns:
        for m in re.finditer(pattern, text):
            entities.append((normalize(m.group()), "HEALTH_UNIT"))

    # =========================
    # 5. DEDUPLICAÇÃO
    # =========================
    unique_entities = {}
    for text, label in entities:

        key = text.lower()

        # prioriza labels mais específicos
        if key not in unique_entities:
            unique_entities[key] = (text, label)

        else:
            existing_label = unique_entities[key][1]

            priority = {
                "DOC_ID": 5,
                "PROCESS_ID": 5,
                "HEALTH_UNIT": 4,
                "ORG": 3,
                "LOCATION": 2,
                "PROGRAM": 1
            }

            if priority.get(label, 0) > priority.get(existing_label, 0):
                unique_entities[key] = (text, label)

    return list(unique_entities.values())

def flatten_corpus(corpus_json):
    docs = []

    for data, content in corpus_json.items():
        edicao = content.get("edicao")

        for pub in content.get("publicacoes", []):

            doc = {
                "id": f"{data}_{pub['id']}",
                "text": pub["texto_completo"],
                "edicao": edicao,
                "data_edicao": pub.get("data_edicao"),
                "tipo": pub.get("tipo_documento"),
                "numero_documento": pub.get("numero_documento"),
                "referencia": pub.get("referencia"),
                "secao": pub.get("secao"),
                "localidades": pub.get("localidades", []),
                "tipo_unidade": pub.get("tipo_unidade", []),
                "topicos": pub.get("topicos", [])
            }

            docs.append(doc)

    return docs


def build_semantic_text(doc):

    meta = []

    if doc["tipo"]:
        meta.append(f"Tipo do documento: {doc['tipo']}")

    if doc.get("numero_documento"):
        meta.append(f"Número do documento: {doc['numero_documento']}")

    if doc.get("referencia"):
        meta.append(f"Referência: {doc['referencia']}")

    if doc.get("edicao"):
        meta.append(f"Edição DODF: {doc['edicao']}")

    if doc.get("data_edicao"):
        meta.append(f"Data da edição: {doc['data_edicao']}")

    if doc["secao"]:
        meta.append(f"Seção: {doc['secao']}")

    meta_text = " | ".join(meta)

    return meta_text, doc["text"]

def build_graph(docs):

    G = nx.Graph()

    for doc in docs:

        text = doc["text"]
        metadata = doc["metadata"]

        # entidades já normalizadas
        ents = extract_entities(text, metadata)

        # adiciona entidades extraídas
        for ent, label in ents:
            G.add_node(ent, label=label, doc_id=doc["id"])

        # 🔴 IMPORTANTE: normalizar metadata também
        for loc in metadata.get("localidades", []):
            loc_norm = normalize(loc)
            G.add_node(loc_norm, label="LOCATION", doc_id=doc["id"])
            ents.append((loc_norm, "LOCATION"))

        for org in metadata.get("tipo_unidade", []):
            org_norm = normalize(org)
            G.add_node(org_norm, label="ORG", doc_id=doc["id"])
            ents.append((org_norm, "ORG"))

        # relações por coocorrência
        for i in range(len(ents)):
            for j in range(i + 1, len(ents)):
                G.add_edge(ents[i][0], ents[j][0], type="co_occurs")

    return G

def graph_retrieve(query, docs, graph, node_list, node_embs, top_k=5):

    # 🔴 normaliza a query para alinhar com o grafo
    query_norm = normalize(query)

    # entidades da query
    query_ents = extract_entities(query_norm)
    query_nodes = [ent for ent, _ in query_ents if ent in graph.nodes]

    # embedding da query
    q_emb = model.encode([query])
    sims = cosine_similarity(q_emb, node_embs)[0]

    # nós iniciais (embedding + entidades)
    top_nodes = set(node_list[i] for i in np.argsort(sims)[-top_k:])
    top_nodes.update(query_nodes)

    # expansão controlada (1-hop)
    expanded_nodes = set(top_nodes)

    for node in list(top_nodes):

        if node not in graph:
            continue

        neighbors = list(graph.neighbors(node))

        for n in neighbors[:10]:  # limite evita explosão
            expanded_nodes.add(n)

    # scoring por documento
    doc_scores = {}

    for node in expanded_nodes:

        doc_id = graph.nodes[node].get("doc_id")
        if not doc_id:
            continue

        label = graph.nodes[node].get("label")

        weight = {
            "DOC_ID": 3,
            "PROCESS_ID": 3,
            "HEALTH_UNIT": 2,
            "ORG": 2,
            "LOCATION": 2,
            "PROGRAM": 1
        }.get(label, 1)

        doc_scores[doc_id] = doc_scores.get(doc_id, 0) + weight

    # ordena documentos
    ranked = sorted(doc_scores.items(), key=lambda x: x[1], reverse=True)
    selected_ids = [doc_id for doc_id, _ in ranked[:top_k]]

    return [d for d in docs if d["id"] in selected_ids]

def chunk_text_recursive(text):

    splitter = RecursiveCharacterTextSplitter(
        chunk_size=1000,
        chunk_overlap=200,
        separators=[
            "\n\n", "\n", ".", ";", ",", " "
        ]
    )

    return splitter.split_text(text)

def build_documents(corpus_json):

    flat_docs = flatten_corpus(corpus_json)
    final_docs = []

    for doc in flat_docs:

        meta_text, raw_text = build_semantic_text(doc)

        if not isinstance(raw_text, str):
            raw_text = str(raw_text)

        locs = doc.get("localidades", [])
        loc_text = f"Localização: {', '.join(locs)}" if locs else ""

        chunks = chunk_text_recursive(raw_text)

        for i, chunk in enumerate(chunks):

            final_docs.append({
                "id": f"{doc['id']}_chunk{i}",
                "text": f"{meta_text}\n{loc_text}\n{chunk}",
                "metadata": doc
            })

    return final_docs

def build_faiss_index(docs):

    texts = [d["text"] for d in docs]
    embeddings = model.encode(texts, show_progress_bar=True)

    dim = embeddings.shape[1]
    index = faiss.IndexFlatIP(dim)

    faiss.normalize_L2(embeddings)
    index.add(embeddings)

    return index, embeddings

def filter_candidates_by_location(query_loc, docs):

    if not query_loc:
        return docs

    filtered = []

    for d in docs:
        doc_locs = [normalize(loc) for loc in d["metadata"].get("localidades", [])]

        if any(match_location(query_loc, loc) for loc in doc_locs):
            filtered.append(d)

    if filtered:
        return filtered
    else:
        return docs

def hybrid_retrieve(query, docs, index, top_k=5):

    known_locations = set()
    for d in docs:
        known_locations.update(d["metadata"].get("localidades", []))


    q_loc = extract_location_normalized(query, known_locations)

    if q_loc:
        query_aug = f"Localização: {q_loc}\n{query}"
    else:
        query_aug = query

    q_emb = model.encode([query_aug])
    faiss.normalize_L2(q_emb)

    D, I = index.search(q_emb, 100)

    scored = []

    for idx, sim in zip(I[0], D[0]):
        doc = docs[idx]

        gscore = geo_score(q_loc, doc)

        score = ALPHA * sim + BETA * gscore

        scored.append((score, doc))

    scored.sort(reverse=True, key=lambda x: x[0])

    return [d for _, d in scored[:top_k]]

def extract_location_normalized(query, known_locations):

    query_norm = normalize(query)

    for loc in known_locations:
        if normalize(loc) in query_norm or query_norm in normalize(loc):
            return normalize(loc)

    return None

